package Parte02.Controle;

public class ForExamples {
  public static void main(String[] args) {
      for (int i = 1; i <= 100; i++) {
          System.out.println(i);
      }

      for (int i = 100; i >= 1; i--) {
          System.out.println(i);
      }

      for (int i = 4; i <= 77; i += 7) {
          System.out.println(i);
      }

      for (int i = 20; i >= 2; i -= 2) {
          System.out.println(i);
      }

      for (int i = 2; i <= 20; i += 3) {
          System.out.println(i);
      }

      for (int i = 99; i >= 0; i -= 11) {
          System.out.println(i);
      }
  }
}